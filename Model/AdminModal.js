import mongoose from 'mongoose';

const AdminModalSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
}, { timestamps: true });

const AdminModal = mongoose.model('Admin', AdminModalSchema);

export default AdminModal;